const filteredProducts = computed(() => {
    if (!Array.isArray(products.value)) {
        console.error('products.value 不是一個陣列');
        return [];
    }

    if (!searchQuery.value) {
        return products.value; // 如果沒有搜尋，顯示所有產品
    }

    // 把搜尋字串拆分成多個條件
    const queryTerms = searchQuery.value
        .split(/\s+/)
        .map((term) => term.trim().toLowerCase())
        .filter(Boolean);

    return products.value.filter((product) => {
        // 檢查每個條件都必須在產品名稱或ID中完全匹配
        return queryTerms.every((term) => {
            const nameMatches = product.name.toLowerCase().includes(term);
            // 對於 ID，必須完全匹配或 ID 以搜尋詞開頭
            const idMatches = product.id.toLowerCase().startsWith(term);
            return nameMatches || idMatches; // 確保名稱或 ID 完全匹配
        });
    });
});
